def draw(board, debug:bool=DEBUG):
    if debug:
        drawing = ""
        for row in range(board.height):
            rowstr = ""
            for col in range(board.width):
                rowstr += " " + str(board.moves[col][row])
            rowstr+= "\n"
            drawing+=rowstr
        print(drawing)

def debug_log(to_print:str):
    if DEBUG:
        print(to_print, flush=True)
